print('''
    Программа длину и площадь окружности.
    На вход принимает радиус окружности. 
    На выходе длина и площадь окружности.
''')

radius = int(input('Введите радиус окружности: '))
pi = 3.14159

print('Длина окружности: ', 2 * pi * radius)
print('Площадь окружности: ', pi * radius ** 2)
